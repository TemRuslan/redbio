	        function setLang(lang) {
	            if (!translations[lang]) lang = 'en';
	            document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
	            document.getElementById(`btn-${lang}`).classList.add('active');

            if (translations[lang] && translations[lang]["page-title"]) {
                document.title = translations[lang]["page-title"];
            }
            
            document.querySelectorAll('[data-key]').forEach(el => {
                const key = el.getAttribute('data-key');
                if (translations[lang][key]) {
                    el.innerHTML = translations[lang][key];
                }
            });

            document.querySelectorAll('[data-placeholder-key]').forEach(el => {
                const key = el.getAttribute('data-placeholder-key');
                if (translations[lang][key]) {
                    el.setAttribute('placeholder', translations[lang][key]);
                }
            });

            document.querySelectorAll('[data-title-key]').forEach(el => {
                const key = el.getAttribute('data-title-key');
                if (translations[lang][key]) {
                    el.setAttribute('title', translations[lang][key].replace(/<br\s*\/?\s*>/gi, ' '));
                }
            });

	            document.documentElement.lang = lang;

	            renderResearch(lang);
	        }

